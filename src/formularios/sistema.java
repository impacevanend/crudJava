    //Métod actualizar datos
      public void ActualizarDatos(){
    
        try {
            
            String SQL = "UPDATE alumnos set nombre=?,apellidos=?,materia=?,"
                        + "calificacion=?,estatus=? WHERE idalumnos =?";
            
            //valor que obtenemos de nuestra fila seleccionada
            int filaSeleccionado  = tablaAlumnnos.getSelectedRow();
            
            //conversión a texto de lo seleccionado por el usuario en la tabla 
            String dao = (String)tablaAlumnnos.getValueAt(filaSeleccionado, 0);//referencia al id
            
            PreparedStatement pst = (PreparedStatement) con.prepareStatement(SQL);
            pst.setString(1, txtNombre.getText());
            pst.setString(2, txtApelliidos.getText());
            
            //Para elegir opción
            int seleccionado = cbMateria.getSelectedIndex();
            pst.setString(3, cbMateria.getItemAt(seleccionado));
            
            pst.setDouble(4, Double.parseDouble(txtCalificacion.getText()));
            
            //Para elegir opción
            int seleccionado1 = cbEstatus.getSelectedIndex();
            pst.setString(5, cbEstatus.getItemAt(seleccionado1));
            
            pst.setString(6, dao);
            //Ejecutar consulta
            pst.execute();
            
            JOptionPane.showMessageDialog(null, "Registro editado con éxito.");
            
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, "Error de edición"+e.getMessage());
        }
    }
    
    //Fin actualizar datos
    
